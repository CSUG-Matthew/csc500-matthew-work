# Ask the user to input two numbers (num1 and num2).
num1 = int(input('Please type a number: '))
num2 = int(input('Please type a second number: '))
sum = num1 + num2
subtract = num1 - num2

# Given those two numbers, add them together to find the output. Also, subtract the two numbers to find the output.
print('The sum of ', num1, ' and ', num2, ' is: ', sum, '.', sep='')
print('And the subtraction of ', num1, ' and ', num2, ' is ', subtract, '.', sep='')